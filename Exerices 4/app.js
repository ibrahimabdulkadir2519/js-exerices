// function declaration
function add(a, b) {
  return a + b;
}

console.log(add(3, 4));   
console.log(add(10, 20)); 
console.log(add(1, 9));   

// function expression
const multiply = function(a, b) {
  return a * b;
};

console.log(multiply(3, 4));   
console.log(multiply(10, 20)); 
console.log(multiply(1, 9));
